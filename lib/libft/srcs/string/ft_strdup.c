#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		s1_len;
	int		i;

	i = 0;
	if (!s1)
		return (NULL);
	s1_len = ft_strlen(s1);
	s2 = (char *)malloc(sizeof(char) * s1_len + 1);
	if (s2 == NULL)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
