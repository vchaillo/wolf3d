#include "libft.h"

float				ft_atof(char *c)
{
	float			f;
	unsigned char	dec;
	int				sign;

	f = 0;
	sign = 1;
	while (*c == ' ' || *c == '\n' || *c == '\t')
		c++;
	if (*c == '-')
	{
		sign = -1;
		c++;
	}
	else if (*c == '+')
		c++;
	while (*c >= '0' && *c <= '9')
		f = (f * 10) + (*c++ - '0');
	dec = 1;
	if (*c++ == '.')
	{
		while (*c >= '0' && *c <= '9')
			f += (*c++ - '0') * pow(10, -(dec++));
	}
	f = f * sign;
	return (f);
}
